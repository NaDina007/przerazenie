package dz001;

public class zd9 {
    public static void main(String[] args) {

        double celsjusza = 6.0;
        double fahrenheita = celsjusza * 1.8 + 32.0;
        double kelwina = celsjusza - 273;

        System.out.println(celsjusza + " C");
        System.out.println(fahrenheita + " F");
        System.out.println(kelwina + " Y");

    }


    }
    //Вивід має бути приблизно таким: X C: Y F; Z K, де X - початкове значення в градусах Цельсія, Y и Z - обчислені значення в Фаренгейтах и Кельвінах відповідно
